class GameBoard {
    constructor(length, width) {
        this.length = length;
        this.width = width;
    }
    getLength() {
        return this.length;
    }
    getWidth() {
        return this.width;
    }
    setLength() {
        this.length = length;
    }
    setWidth() {
        this.width = width;
    }
}